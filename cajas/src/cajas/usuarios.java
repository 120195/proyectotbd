    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
       
       
        try {
            PreparedStatement psd = cn.prepareStatement("INSERT INTO users (user_name,user_password,paterno,materno,nombre,ventas,administrar,reportes,catalogos,consultas,deshacer_venta) VALUES (?,?,?,?,?,?,?,?,?,?,?) ");
            psd.setString(1, nombre_usuario.getText());
            psd.setString(2, contraseña.getText());
            psd.setString(3, apell_paterno.getText());
            psd.setString(4, apell_materno.getText());
            psd.setString(5, nombre.getText());
            psd.setString(6, ventas.getText());
            psd.setString(7, administrar.getText());
            psd.setString(8, reportes.getText());
            psd.setString(9, catalogos.getText());
            psd.setString(10, consultas.getText());
            psd.setString(11, desha_venta.getText());
            
            
            int n=psd.executeUpdate();
            mostrardatos("");
            
            if(n>0){
                JOptionPane.showMessageDialog(null, "registro guardado");
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_guardarActionPerformed

