    void mostrardatos(String valor){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id_user");
        modelo.addColumn("nombre_usuario");
        modelo.addColumn("contraseña");
        modelo.addColumn("apellido paterno");
        modelo.addColumn("apellido_materno");
        modelo.addColumn("nombre");
        modelo.addColumn("ventas");
        modelo.addColumn("administrar");
        modelo.addColumn("reportes");
        modelo.addColumn("catalogos");
        modelo.addColumn("consultas");
        modelo.addColumn("deshacer venta");
        tb_usuarios.setModel(modelo);
         String sql="";
        if(valor.equals("")){
            
            sql="SELECT * FROM users";
            
        }
        
        else{
            
            sql="SELECT * FROM users WHERE id_user='"+valor+"'";
        }
        
        String [] datos = new String [12];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                
                
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                datos[7]=rs.getString(8);
                datos[8]=rs.getString(9);
                datos[9]=rs.getString(10);
                datos[10]=rs.getString(11);
                datos[11]=rs.getString(12);
                modelo.addRow(datos);
                
                
            }
                    
           tb_usuarios.setModel(modelo);     
            
    
        
        } catch (SQLException ex) {
            Logger.getLogger(usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

