    void mostrardatos(String valor){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id_producto");
        modelo.addColumn("folio");
        modelo.addColumn("cantidad");
        modelo.addColumn("precio_unitario");
        modelo.addColumn("iva");
        tbdet_venta.setModel(modelo);
        
        String sql="";
        if(valor.equals("")){
            
            sql="SELECT * FROM detalle_ventas";
            
        }
        else{
            
            sql="SELECT * FROM detalle_ventas WHERE idproducto='"+valor+"'";
        }
        String [] datos = new String [5];
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                modelo.addRow(datos);
                
            }
            tbdet_venta.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(detalle_venta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

