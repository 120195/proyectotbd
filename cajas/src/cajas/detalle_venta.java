    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
        // TODO add your handling code here:
        
       
        try {
            PreparedStatement psd = cn.prepareStatement("INSERT INTO detalle_ventas (idproducto,id_venta,cantidad,p_unitario,iva) VALUES (?,?,?,?,?)");
            
            psd.setNString(1, id_producto.getText());
            psd.setNString(2,folio.getText());
            psd.setNString(3, cantidad.getText());
            psd.setNString(4, precio_unitario.getText());
            psd.setNString(5, iva.getText());
            int n=psd.executeUpdate();
            mostrardatos("");
            if(n>0){
                
                JOptionPane.showMessageDialog(null, "registro guardado");
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(detalle_venta.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_guardarActionPerformed

