    void mostrardatos(String valor){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("id_caja");
        modelo.addColumn("descu_caja");
        modelo.addColumn("disponible");
        modelo.addColumn("serie");
        modelo.addColumn("nombre_usuario");
        modelo.addColumn("folio actual");
        tb_cajas.setModel(modelo);
        String sql="";
        if(valor.equals("")){
            
            sql="SELECT * FROM cat_cajas";
            
        }
        else{
            sql="SELECT * FROM cat_cajas WHERE id_caja='"+valor+"'";
        }
        String [] datos = new String[6];
        try {
            
            Statement st = cn.createStatement();
            
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                
                datos[0]=rs.getString(1);
                datos[1]=rs.getString(2);
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                modelo.addRow(datos);
            }
            tb_cajas.setModel(modelo);
            
        } catch (SQLException ex) {
            Logger.getLogger(cajass.class.getName()).log(Level.SEVERE, null, ex);
}
}
   
    
   

