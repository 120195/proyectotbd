    private void guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarActionPerformed
    
    
    
    
        try {
            PreparedStatement psd = cn.prepareStatement("INSERT INTO cat_cajas (desc_caja,disponible,id_user,serie,folio_actual) VALUES (?,?,?,?,?)");
            
            psd.setString(1,des_caja.getText());
            psd.setString(2,disponible.getText());
            psd.setString(3,nombre_usuario.getText());
            psd.setNString(4,tiket.getText());
            psd.setNString(5,folioa.getText());
            
            int n=psd.executeUpdate();
            mostrardatos("");
            
            if (n>0){
                
                JOptionPane.showMessageDialog(null,"registro guardado");
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(cajass.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_guardarActionPerformed

